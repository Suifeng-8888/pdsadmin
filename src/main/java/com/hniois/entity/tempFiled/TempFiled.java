package com.hniois.entity.tempFiled;

import com.hniois.common.page.Page;

/**
*  模板明细 Bean
*/
public class TempFiled extends Page{
    /**基础字段
     ************************************************************/
    private String id; //主键KEY
    private String temp_id; //模版主表KEY
    private String filed_id; //Key
    private String filed_name; //名称
    private String filed_type; //类型
    private String filed_order; //长度
    private String filed_comment; //注释
    private String filed_default; //默认值
    private String filed_check; //验证
    /*************************************************************/

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTemp_id() {
        return temp_id;
    }

    public void setTemp_id(String temp_id) {
        this.temp_id = temp_id;
    }

    public String getFiled_id() {
        return filed_id;
    }

    public void setFiled_id(String filed_id) {
        this.filed_id = filed_id;
    }

    public String getFiled_name() {
        return filed_name;
    }

    public void setFiled_name(String filed_name) {
        this.filed_name = filed_name;
    }

    public String getFiled_type() {
        return filed_type;
    }

    public void setFiled_type(String filed_type) {
        this.filed_type = filed_type;
    }

    public String getFiled_order() {
        return filed_order;
    }

    public void setFiled_order(String filed_order) {
        this.filed_order = filed_order;
    }

    public String getFiled_comment() {
        return filed_comment;
    }

    public void setFiled_comment(String filed_comment) {
        this.filed_comment = filed_comment;
    }

    public String getFiled_default() {
        return filed_default;
    }

    public void setFiled_default(String filed_default) {
        this.filed_default = filed_default;
    }

    public String getFiled_check() {
        return filed_check;
    }

    public void setFiled_check(String filed_check) {
        this.filed_check = filed_check;
    }


}

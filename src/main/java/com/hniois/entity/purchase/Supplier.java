package com.hniois.entity.purchase;

import com.hniois.common.page.Page;

public class Supplier extends Page {

    private String sup_id;//供应商ID
    private String name;//供应商名字
    private String address;//供应商地址
    private String contacts;//联系人
    private String telephone;//手机号
    private String email;//电子邮箱
    private String temp_price;//临时价格
    private String bz;//备用

    public String getSup_id() {
        return sup_id;
    }

    public void setSup_id(String sup_id) {
        this.sup_id = sup_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getContacts() {
        return contacts;
    }

    public void setContacts(String contacts) {
        this.contacts = contacts;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTemp_price() {
        return temp_price;
    }

    public void setTemp_price(String temp_price) {
        this.temp_price = temp_price;
    }

    public String getBz() {
        return bz;
    }

    public void setBz(String bz) {
        this.bz = bz;
    }
}


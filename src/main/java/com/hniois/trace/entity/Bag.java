package com.hniois.trace.entity;

import com.hniois.common.page.Page;

import java.util.List;

public class Bag extends Page {
    private String pId;
    private String theID;//部门编码
    private String name;//部门名称
    private String bz;//备注
    /*************************************************************/
    private String pname; //上一层级名称
    private List<Bag> children; //tree子集
    private Boolean spread = true; //tree 默认展开

    public String getpId() {
        return pId;
    }

    public void setpId(String pId) {
        this.pId = pId;
    }

    public String getTheID() {
        return theID;
    }

    public void setTheID(String theID) {
        this.theID = theID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBz() {
        return bz;
    }

    public void setBz(String bz) {
        this.bz = bz;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public List<Bag> getChildren() {
        return children;
    }

    public void setChildren(List<Bag> children) {
        this.children = children;
    }

    public Boolean getSpread() {
        return spread;
    }

    public void setSpread(Boolean spread) {
        this.spread = spread;
    }
}
